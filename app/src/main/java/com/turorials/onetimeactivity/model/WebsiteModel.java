package com.turorials.onetimeactivity.model;

public class WebsiteModel {
    String website_logo;
    String website_title;
    String website_main_image;
    String website_link;



    public String getWebsite_logo() {
        return website_logo;
    }

    public String getWebsite_title() {
        return website_title;
    }

    public String getWebsite_main_image() {
        return website_main_image;
    }

    public String getWebsite_link() {
        return website_link;
    }


    public void setWebsite_logo(String website_logo) {
        this.website_logo = website_logo;
    }

    public void setWebsite_title(String website_title) {
        this.website_title = website_title;
    }

    public void setWebsite_main_image(String website_main_image) {
        this.website_main_image = website_main_image;
    }

    public void setWebsite_link(String website_link) {
        this.website_link = website_link;
    }
}
