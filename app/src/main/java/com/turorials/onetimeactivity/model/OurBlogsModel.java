package com.turorials.onetimeactivity.model;

public class OurBlogsModel {
    String image_url;
    String title;
    String first_line_text;
    String blog_url;
    String upload_time;


    public String getImage_url() {
        return image_url;
    }

    public void setImage_url(String image_url) {
        this.image_url = image_url;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getFirst_line_text() {
        return first_line_text;
    }

    public void setFirst_line_text(String first_line_text) {
        this.first_line_text = first_line_text;
    }

    public String getBlog_url() {
        return blog_url;
    }

    public void setBlog_url(String blog_url) {
        this.blog_url = blog_url;
    }

    public String getUpload_time() {
        return upload_time;
    }

    public void setUpload_time(String upload_time) {
        this.upload_time = upload_time;
    }
}
